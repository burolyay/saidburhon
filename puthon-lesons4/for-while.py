def kalkulyator(son1, son2, belgi):
    if belgi == "+":
        x = son1 + son2
    elif belgi == "-":
        x = son1 - son2
    elif belgi == "*":
        x = son1 * son2
    elif belgi == "/":
        x = son1 / son2
    elif belgi == "//":
        x = son1 // son2
    elif belgi == "**":
        x = son1 ** son2
    elif belgi == "%":
        x = son1 % son2
    return x

